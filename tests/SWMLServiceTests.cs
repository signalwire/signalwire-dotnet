    [Fact]
    public void ProxyUrl_FromEnv()
    {
        Environment.SetEnvironmentVariable("SWML_PROXY_URL_BASE", "https://proxy.example.com");
        var svc = MakeService();
        Assert.Equal("https://proxy.example.com", svc.GetProxyUrlBase());
    }

    [Fact]
    public void ProxyUrl_FromForwardedHeaders()
    {
        var svc = MakeService();
        var headers = new Dictionary<string, string>
        {
            ["X-Forwarded-Proto"] = "https",
            ["X-Forwarded-Host"] = "myapp.example.com",
        };
        Assert.Equal("https://myapp.example.com", svc.GetProxyUrlBase(headers));
    }

    [Fact]
    public void ProxyUrl_FromOriginalUrl()
    {
        var svc = MakeService();
        var headers = new Dictionary<string, string>
        {
            ["X-Original-URL"] = "https://original.example.com/path",
        };
        Assert.Equal("https://original.example.com/path", svc.GetProxyUrlBase(headers));
    }

    [Fact]
    public void ProxyUrl_Fallback()
    {
        var svc = MakeService(host: "0.0.0.0", port: 3000);
        Assert.Equal("http://0.0.0.0:3000", svc.GetProxyUrlBase());
    }

    // ------------------------------------------------------------------
    // Routing Callbacks
    // ------------------------------------------------------------------

    [Fact]
    public void RoutingCallback_Dispatched()
    {
        var svc = MakeService();
        var called = false;
        svc.RegisterRoutingCallback("/custom", (data, headers) =>
        {
            called = true;
            return new Dictionary<string, object> { ["custom"] = "response" };
        });

        var (status, _, body) = svc.HandleRequest("POST", "/custom", AuthHeader(), "{}");
        Assert.True(called);
        Assert.Equal(200, status);
        Assert.Contains("response", body);
    }
