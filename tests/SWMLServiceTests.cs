using System.Text;
using Xunit;
using SignalWire.SWML;
using SignalWire.Logging;

namespace SignalWire.Tests;

public class SWMLServiceTests : IDisposable
{
    public SWMLServiceTests()
    {
        Schema.Reset();
        Logger.Reset();
        Environment.SetEnvironmentVariable("SWML_BASIC_AUTH_USER", null);
        Environment.SetEnvironmentVariable("SWML_BASIC_AUTH_PASSWORD", null);
        Environment.SetEnvironmentVariable("SWML_PROXY_URL_BASE", null);
        Environment.SetEnvironmentVariable("PORT", null);
    }

    public void Dispose()
    {
        Schema.Reset();
        Logger.Reset();
        Environment.SetEnvironmentVariable("SWML_BASIC_AUTH_USER", null);
        Environment.SetEnvironmentVariable("SWML_BASIC_AUTH_PASSWORD", null);
        Environment.SetEnvironmentVariable("SWML_PROXY_URL_BASE", null);
        Environment.SetEnvironmentVariable("PORT", null);
    }

    private static Service MakeService(
        string name = "test-service",
        string? route = null,
        string? host = null,
        int? port = null,
        string? basicAuthUser = "testuser",
        string? basicAuthPassword = "testpass")
    {
        return new Service(new ServiceOptions
        {
            Name = name,
            Route = route ?? "/",
            Host = host ?? "0.0.0.0",
            Port = port,
            BasicAuthUser = basicAuthUser,
            BasicAuthPassword = basicAuthPassword,
        });
    }

    private static Dictionary<string, string> AuthHeader(string user = "testuser", string pass = "testpass")
    {
        var encoded = Convert.ToBase64String(Encoding.UTF8.GetBytes($"{user}:{pass}"));
        return new Dictionary<string, string> { ["Authorization"] = $"Basic {encoded}" };
    }

    // ------------------------------------------------------------------
    // Construction
    // ------------------------------------------------------------------

    [Fact]
    public void Construction_Defaults()
    {
        var svc = MakeService();
        Assert.Equal("test-service", svc.Name);
        Assert.Equal("/", svc.Route);
        Assert.Equal("0.0.0.0", svc.Host);
        Assert.Equal(3000, svc.Port);
    }

    [Fact]
    public void CustomRoute()
    {
        var svc = MakeService(route: "/myagent");
        Assert.Equal("/myagent", svc.Route);
    }

    [Fact]
    public void TrailingSlash_Stripped()
    {
        var svc = MakeService(route: "/myagent/");
        Assert.Equal("/myagent", svc.Route);
    }

    [Fact]
    public void Port_FromEnv()
    {
        Environment.SetEnvironmentVariable("PORT", "8080");
        var svc = new Service(new ServiceOptions
        {
            Name = "test",
            BasicAuthUser = "u",
            BasicAuthPassword = "p",
        });
        Assert.Equal(8080, svc.Port);
    }

    // ------------------------------------------------------------------
    // Auth
    // ------------------------------------------------------------------

    [Fact]
    public void ExplicitAuth()
    {
        var svc = MakeService(basicAuthUser: "myuser", basicAuthPassword: "mypass");
        var (user, pass) = svc.GetBasicAuthCredentials();
        Assert.Equal("myuser", user);
        Assert.Equal("mypass", pass);
    }

    [Fact]
    public void EnvAuth()
    {
        Environment.SetEnvironmentVariable("SWML_BASIC_AUTH_USER", "envuser");
        Environment.SetEnvironmentVariable("SWML_BASIC_AUTH_PASSWORD", "envpass");
        var svc = new Service(new ServiceOptions { Name = "test" });
        var (user, pass) = svc.GetBasicAuthCredentials();
        Assert.Equal("envuser", user);
        Assert.Equal("envpass", pass);
    }

    [Fact]
    public void AutoGeneratedAuth()
    {
        var svc = new Service(new ServiceOptions { Name = "test" });
        var (user, pass) = svc.GetBasicAuthCredentials();
        Assert.NotEmpty(user);
        Assert.NotEmpty(pass);
        Assert.True(user.Length > 8);
        Assert.True(pass.Length > 8);
    }

    [Fact]
    public void AutoGeneratedAuth_DifferentEachTime()
    {
        var svc1 = new Service(new ServiceOptions { Name = "test1" });
        var svc2 = new Service(new ServiceOptions { Name = "test2" });
        var (user1, _) = svc1.GetBasicAuthCredentials();
        var (user2, _) = svc2.GetBasicAuthCredentials();
        Assert.NotEqual(user1, user2);
    }

    [Fact]
    public void GetFullUrl_WithoutAuth()
    {
        var svc = MakeService();
        Assert.Equal("http://0.0.0.0:3000/", svc.GetFullUrl());
    }

    [Fact]
    public void GetFullUrl_WithAuth()
    {
        var svc = MakeService(basicAuthUser: "u", basicAuthPassword: "p");
        Assert.Equal("http://u:p@0.0.0.0:3000/", svc.GetFullUrl(includeAuth: true));
    }

    [Fact]
    public void GetFullUrl_WithRoute()
    {
        var svc = MakeService(route: "/agent");
        Assert.Equal("http://0.0.0.0:3000/agent", svc.GetFullUrl());
    }

    // ------------------------------------------------------------------
    // Verb methods
    // ------------------------------------------------------------------

    [Fact]
    public void Verb_AddsToDocument()
    {
        var svc = MakeService();
        svc.Verb("answer", new Dictionary<string, object> { ["max_duration"] = 3600 });

        var verbs = svc.Document.GetVerbs("main");
        Assert.Single(verbs);
        Assert.True(verbs[0].ContainsKey("answer"));
    }

    [Fact]
    public void Sleep_TakesInteger()
    {
        var svc = MakeService();
        svc.Sleep(2000);

        var verbs = svc.Document.GetVerbs("main");
        Assert.Single(verbs);
        Assert.True(verbs[0].ContainsKey("sleep"));
        Assert.Equal(2000, verbs[0]["sleep"]);
    }

    [Fact]
    public void Verb_Chaining()
    {
        var svc = MakeService();
        var result = svc.Verb("answer", new Dictionary<string, object> { ["max_duration"] = 3600 });
        Assert.Same(svc, result);
    }

    [Fact]
    public void AllSchemaVerbs_Callable()
    {
        var svc = MakeService();
        var schema = Schema.Instance;
        var names = schema.GetVerbNames();

        foreach (var verb in names)
        {
            if (verb == "sleep")
            {
                svc.Sleep(1000);
            }
            else
            {
                svc.Verb(verb, new Dictionary<string, object>());
            }
        }

        var verbs = svc.Document.GetVerbs("main");
        Assert.Equal(names.Count, verbs.Count);
    }

    [Fact]
    public void UnknownVerb_Throws()
    {
        var svc = MakeService();
        Assert.Throws<ArgumentException>(() => svc.Verb("nonexistent_verb", new Dictionary<string, object>()));
    }

